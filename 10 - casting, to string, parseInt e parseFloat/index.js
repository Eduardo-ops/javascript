// Parseando de string para int

// Variáveis com valores do tipo string
var v1 = '1'
var v2 = '2'

// Transformando variáveis com valor string para valor int
v1 = parseInt(v1)
v2 = parseInt(v2)

// Realizando soma nesse caso
console.log(v1 + v2)

// Parseando de string para float
var v3 = '1.23'
var v4 = '2.45'

v3 = parseFloat(v3)
v4 = parseFloat(v4)

console.log(v3 = v4)

// Representando variáveis do tipo int como string
